            <h1>Le diagnostic de dyspraxie</h1>
            <h2>Comment dois-je interpréter le diagnostic de dyspraxie ou de Trouble de l'Acquisition de la Coordination (TAC) ?</h2>
        </header>
        <p>La dyspraxie se définit comme étant un trouble de l'élaboration et de l'automatisation des gestes moteurs pour réaliser une action ou une activité.</p>
        <p>La dyspraxie et le TAC, qui sont des diagnostics médicaux, font partie des troubles neurodéveloppementaux, c'est-à-dire en lien avec des atteintes diffuses du cerveau de l'enfant.</p>
        <p>Les manifestations de la dyspraxie ou du TAC sont multiples et interfèrent de façon significative avec l'acquisition de l'autonomie dans plusieurs sphères de la vie de l'enfant.</p>
        <p>L'enfant présente donc un trouble neurologique qui limite sa capacité à développer une maîtrise acceptable des gestes moteurs en dépit de ses efforts et de la stimulation reçue.</p>
        
        <h2>Ce que le parent doit retenir:</h2>
            <ul>
                <li>les difficultés ne s'expliquent pas par une faiblesse des bras ou des jambes</li>
                <li>le manque de motivation, la paresse ou un manque d'intérêt à apprendre ne sont pas en cause</li>
                <li>la maladresse ou les erreurs ne sont pas le fait d'un trouble déficitaire de l'attention (TDA/H) ou d'un trouble de comportement</li>
                <li>la compréhension des consignes n'est pas en cause</li>
                <li>la dyspraxie n'est pas synonyme de déficience intellectuelle</li>
                <li>les difficultés ne sont pas causées par un trouble moteur primaire (déficience motrice cérébrale, atteinte lésionnelle du cerveau, ataxie, etc.)</li>
            </ul>
